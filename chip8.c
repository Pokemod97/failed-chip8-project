void Run(){
  initChip(getFile());

}

 void initChip(uint8_t *file){
    // insert chip 8 font set
    for(int i = 0; i < 80;i++){
      memory[i] = chip8_fontset[i];
    }
   // load program into memory
   for(int i = 0; i < sizeof(file); i++){
     memory[i + 0x200] = file[i];
   }
}
